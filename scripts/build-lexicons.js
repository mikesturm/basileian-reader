#!/usr/bin/env node
'use strict';

/**
 * build-lexicons.js
 *
 * Builds all lexicon files the Basileian Reader needs from the locally
 * installed `strongs` npm package (word-form → Strong's number via
 * normalised-lemma matching, plus a full definitions index).
 *
 * Outputs:
 *   lexicons/strongs-index.json   — { "G746": { number, lemma, definition } }
 *   lexicons/word-strongs-map.json — { "αρχη": "G746", ... }
 *   lexicons/verse-words.json     — { "mark.1.1": [{text, strongs}], ... }
 *
 * The verse-words map is consumed by app.js at runtime to hydrate word-level
 * Strong's attributes. It is generated by tokenising each verse in the 18
 * tier JSON files using normalised-lemma matching.
 *
 * Coverage notes:
 *   - Function words (articles, prepositions, conjunctions) match well because
 *     they are undeclined or appear frequently in dictionary form.
 *   - Inflected nouns/verbs may not match if their form diverges from the lemma.
 *   - Coptic (Thomas) verses are skipped — no Strong's for Sahidic.
 *   - When fetch-strongs.js or fetch-opengnt.js are fixed with working URLs,
 *     running those scripts and then re-running this one will improve coverage
 *     because the tier files will already have word arrays to fall back on.
 *
 * Usage:
 *   node scripts/build-lexicons.js
 */

const fs = require('fs');
const path = require('path');

const LEXICONS_DIR = 'lexicons';
// OpenGNT_version3_3.csv (downloaded by fetch-opengnt.js)
const OPENGNT_CSV = 'scripts/cache/OpenGNT_version3_3.csv';
// Unicode separators used in compound columns of the OpenGNT CSV
const COMPOUND_START = '〔'; // 〔
const COMPOUND_END   = '〕'; // 〕
const FIELD_SEP      = '｜'; // ｜
const TIER_FILES = [
  'tier1_a_mark.json',
  'tier1_b_q.json',
  'tier1_c_special_l.json',
  'tier1_d_special_m.json',
  'tier1_e_thomas.json',
  'tier1_f_john.json',
  'tier2_a_last_supper.json',
  'tier2_b_appearances.json',
  'tier2_c_pauline_claims.json',
  'tier2_d_ascension.json',
  'tier2_e_agraphon.json',
  'tier2_f_didache.json',
  'tier3_a_egerton.json',
  'tier3_b_poxy_1224.json',
  'tier3_c_poxy_840.json',
  'tier3_d_poxy_5575.json',
  'tier3_e_gospel_of_peter.json',
  'tier3_f_jewish_christian.json',
];

// ---------------------------------------------------------------------------
// Greek normalisation
// ---------------------------------------------------------------------------

// Map any polytonic / diacritical Greek character to its plain base letter.
// Built by decomposing all Unicode chars in the Greek Extended block (1F00-1FFF)
// and the Combining Diacritical Marks block (0300-036F).
function normalizeGreek(str) {
  if (!str) return '';
  const decomposed = str.normalize('NFD')
    .replace(/[̀-ͯ᷀-᷿⃐-⃿]/g, '');
  return decomposed
    .toLowerCase()
    .replace(/ς/g, 'σ')  // treat final and medial sigma identically
    .replace(/ϲ/g, 'σ'); // OpenGNT uses lunate sigma ϲ (U+03F2)
}

// ---------------------------------------------------------------------------
// Build Strong's index and lemma map from the `strongs` npm package
// ---------------------------------------------------------------------------

function buildFromStrongsPackage() {
  let strongsPkg;
  try {
    strongsPkg = require('strongs');
  } catch (e) {
    console.error('❌ The `strongs` npm package is not installed.');
    console.error('   Run: npm install strongs');
    process.exit(1);
  }

  const strongsIndex = {};   // G-number → { number, lemma, definition, kjv }
  const wordStrongsMap = {}; // normalised word form → G-number

  for (const [key, val] of Object.entries(strongsPkg)) {
    if (!key.startsWith('G') && !key.startsWith('H')) continue;
    if (!key.startsWith('G')) continue; // Greek only for now

    const entry = {
      number: key,
      lemma: val.lemma || '',
      definition: [val.strongs_def, val.kjv_def].filter(Boolean).join(' | ') || key,
    };
    strongsIndex[key] = entry;

    // Index by normalised lemma form
    if (val.lemma) {
      const norm = normalizeGreek(val.lemma);
      if (norm && !wordStrongsMap[norm]) {
        wordStrongsMap[norm] = key;
      }
    }
  }

  console.log(`✓ Built Strong's index: ${Object.keys(strongsIndex).length} entries`);
  console.log(`✓ Built word→Strong's map: ${Object.keys(wordStrongsMap).length} entries`);
  return { strongsIndex, wordStrongsMap };
}

// ---------------------------------------------------------------------------
// Load OpenGNT word-form → Strong's map (if CSV is available)
// ---------------------------------------------------------------------------

function loadOpenGNTWordMap() {
  if (!fs.existsSync(OPENGNT_CSV)) {
    console.log('  (OpenGNT CSV not found — using lemma-only fallback)');
    return null;
  }

  console.log(`  Loading ${OPENGNT_CSV}…`);
  const csv = fs.readFileSync(OPENGNT_CSV, 'utf8');
  const lines = csv.split('\n');
  const wordMap = {}; // normalised word form → Strong's number

  for (let i = 1; i < lines.length; i++) { // skip header
    const line = lines[i];
    if (!line) continue;
    const cols = line.split('\t');
    if (cols.length < 8) continue;

    // col[7] = 〔OGNTk｜OGNTu｜OGNTa｜lexeme｜rmac｜sn〕
    const compound = cols[7];
    if (!compound) continue;
    const inner = compound.slice(
      compound.indexOf(COMPOUND_START) + 1,
      compound.lastIndexOf(COMPOUND_END)
    );
    const parts = inner.split(FIELD_SEP);
    if (parts.length < 6) continue;

    const ogntu = parts[1]?.trim(); // unaccented form (OGNTu)
    const ognta = parts[2]?.trim(); // accented form (OGNTa)
    const lexeme = parts[3]?.trim();
    let sn = parts[5]?.trim();      // Strong's number

    if (!sn || !sn.startsWith('G')) continue;
    // Extended Strong's sometimes appends a letter (G1234a); strip it
    sn = sn.replace(/^(G\d+)[a-z]$/, '$1');

    // Index all forms: OGNTu (already unaccented), OGNTa (accented), lexeme
    for (const form of [ogntu, ognta, lexeme]) {
      if (!form) continue;
      const norm = normalizeGreek(form);
      if (norm && !wordMap[norm]) {
        wordMap[norm] = sn;
      }
    }
  }

  console.log(`  ✓ OpenGNT: ${Object.keys(wordMap).length} form→Strong's entries`);
  return wordMap;
}

// ---------------------------------------------------------------------------
// Tokenise corpus tier files → verse-words map
// ---------------------------------------------------------------------------

function tokeniseVerse(text, wordStrongsMap) {
  if (!text) return [];
  // Split on whitespace; strip punctuation wrappers but keep the word.
  return text
    .split(/\s+/)
    .filter(w => w.length > 0)
    .map(raw => {
      const clean = raw.replace(/^[^Ͱ-Ͽἀ-῿]+|[^Ͱ-Ͽἀ-῿]+$/g, '');
      const norm = normalizeGreek(clean || raw);
      const strongs = wordStrongsMap[norm] || null;
      const token = { text: clean || raw };
      if (strongs) token.strongs = strongs;
      return token;
    });
}

function buildVerseWordsMap(wordStrongsMap, openGNTMap) {
  // Merge: OpenGNT inflected-form entries take precedence over lemma-only entries
  const combinedMap = openGNTMap
    ? Object.assign({}, wordStrongsMap, openGNTMap)
    : wordStrongsMap;
  const verseWords = {};
  let totalVerses = 0;
  let tokenisedVerses = 0;
  let wordsWithStrongs = 0;
  let totalWords = 0;

  for (const tierFile of TIER_FILES) {
    if (!fs.existsSync(tierFile)) continue;
    const data = JSON.parse(fs.readFileSync(tierFile, 'utf8'));
    const language = data.language || 'grc';

    // Skip non-Greek sections (Coptic Thomas has no Strong's)
    if (language === 'cop') continue;

    for (const pericope of data.pericopes || []) {
      for (const verse of pericope.verses || []) {
        if (!verse.verse_id || !verse.text) continue;
        totalVerses++;

        // Prefer existing .words array (pre-tokenised); otherwise use combined map.
        const words = Array.isArray(verse.words) && verse.words.length > 0
          ? verse.words
          : tokeniseVerse(verse.text, combinedMap);

        verseWords[verse.verse_id] = words;
        totalWords += words.length;
        const matched = words.filter(w => w.strongs).length;
        if (matched > 0) tokenisedVerses++;
        wordsWithStrongs += matched;
      }
    }
  }

  const pct = totalWords > 0 ? Math.round(wordsWithStrongs / totalWords * 100) : 0;
  console.log(`✓ Verse-words map: ${Object.keys(verseWords).length} verses`);
  console.log(`  ${wordsWithStrongs} / ${totalWords} words matched to a Strong's number (${pct}%)`);
  if (pct < 60) {
    console.warn(`  ⚠  Coverage is low. Run scripts/fetch-opengnt.js then re-run this script.`);
  }

  return verseWords;
}

// ---------------------------------------------------------------------------
// Write output files
// ---------------------------------------------------------------------------

function writeJSON(filePath, obj) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, JSON.stringify(obj) + '\n', 'utf8');
  const kb = Math.round(fs.statSync(filePath).size / 1024);
  console.log(`  → ${filePath} (${kb} KB)`);
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  console.log('🔧 Basileian Lexicon Builder\n');

  const { strongsIndex, wordStrongsMap } = buildFromStrongsPackage();

  console.log('\n📖 Loading OpenGNT inflected-form data...');
  const openGNTMap = loadOpenGNTWordMap();

  console.log('\n📖 Tokenising corpus...');
  const verseWords = buildVerseWordsMap(wordStrongsMap, openGNTMap);

  console.log('\n💾 Writing lexicon files...');
  writeJSON(path.join(LEXICONS_DIR, 'strongs-index.json'), strongsIndex);
  writeJSON(path.join(LEXICONS_DIR, 'word-strongs-map.json'), wordStrongsMap);
  writeJSON(path.join(LEXICONS_DIR, 'verse-words.json'), verseWords);

  console.log('\n✅ Done. The app will load word-level Strong\'s data from lexicons/verse-words.json');
}

main();
